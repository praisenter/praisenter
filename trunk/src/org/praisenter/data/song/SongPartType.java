package org.praisenter.data.song;

/**
 * Enumeration of the song part types.
 * @author William Bittle
 * @version 1.0.0
 * @since 1.0.0
 */
public enum SongPartType {
	/** Verse part */
	VERSE,
	
	/** Chorus part */
	CHORUS,
	
	/** Bridge part */
	BRIDGE,
	
	/** End part */
	END,
	
	/** Vamp part */
	VAMP,
	
	/** Tag part */
	TAG,
	
	/** Other part */
	OTHER
}
