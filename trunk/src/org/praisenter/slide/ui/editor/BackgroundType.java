package org.praisenter.slide.ui.editor;

/**
 * Enumeration of the supported background types.
 * @author William Bittle
 * @version 2.0.0
 * @since 2.0.0
 */
public enum BackgroundType {
	/** An empty background (no background) */
	NONE,
	
	/** A color or gradient background */
	PAINT,
	
	/** An image background */
	IMAGE,
	
	/** A video background */
	VIDEO
}
