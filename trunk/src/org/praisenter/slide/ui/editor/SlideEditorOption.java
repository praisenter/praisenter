package org.praisenter.slide.ui.editor;

/**
 * Enumeration of slide editing options.
 * @author William Bittle
 * @version 2.0.0
 * @since 2.0.0
 */
public enum SlideEditorOption {
	/** The user saved a slide/template */
	SAVE,
	
	/** The user saved a new slide/template */
	SAVE_AS,
	
	/** The user canceled the edits */
	CANCEL
}
